//==============================================================================
// STR16_BIG:
//==============================================================================
void STR16_BIG(uint8_t* pDesbuff, uint16_t Sourdata)
{
    *pDesbuff       = (uint8_t)(Sourdata >> 8);
    *(pDesbuff + 1) = (uint8_t)(Sourdata & 0x00ff);
}

//==============================================================================
// STR32_BIG:
//==============================================================================
void STR32_BIG(uint8_t* pDesbuff, uint32_t Sourdata)
{
	uint16_t data_tmp = (uint16_t)(Sourdata >> 16);
	STR16_BIG(pDesbuff,data_tmp);

    data_tmp = (uint16_t)(Sourdata & 0x0000ffff);
	STR16_BIG((pDesbuff+2),data_tmp);
}

//==============================================================================
// LDR16_BIG:
//==============================================================================
uint16_t LDR16_BIG(uint8_t* pSourcbuff)
{
	return ((*pSourcbuff) << 8 | (*(pSourcbuff+1)));
}

//==============================================================================
// LDR32_BIG:
//==============================================================================
uint32_t LDR32_BIG(uint8_t* pSourcbuff)
{
	return 
	( 
		((*pSourcbuff) << 24)
	  | ((*(pSourcbuff + 1)) << 16)
	  | ((*(pSourcbuff + 2)) << 8)
	  | (*(pSourcbuff + 3))
	);
}

